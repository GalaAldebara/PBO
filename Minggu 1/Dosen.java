public class Dosen {
    public String nama;
    public int noDosen;
    public String alamat;
    public String jeniskelamin;
    public String noTelp;

    public void Dosen(String nama, int noDosen, String alamat, String jeniskelamin, String noTelp) {
        this.nama = nama;
        this.noDosen = noDosen;
        this.alamat = alamat;
        this.jeniskelamin = jeniskelamin;
        this.noTelp = noTelp;
    }
}
